/* 1. MONTHLY SUBSCRIBERS GROWTH VS. CHURN TREND  
Business Question : How has our subscriber base grown month over month 
& how many users churned each month ? */

WITH monthly_joins AS (
    SELECT 
        YEAR(CAST(registration_date AS DATE)) * 100 + 
        MONTH(CAST(registration_date AS DATE))      AS month_year,
        COUNT(user_id)                              AS new_subscribers
    FROM subscribers
    GROUP BY 
        YEAR(CAST(registration_date AS DATE)) * 100 + 
        MONTH(CAST(registration_date AS DATE))
), -- CTE to count new sign-ins per month 

monthly_churns AS (
    SELECT 
        YEAR(CAST(churn_date AS DATE)) * 100 + 
        MONTH(CAST(churn_date AS DATE))             AS month_year, -- converting date to YYYY-MM format to group the data by month easily 
        COUNT(user_id)                              AS churned_users
    FROM subscribers
    WHERE churn_date IS NOT NULL
    GROUP BY 
        YEAR(CAST(churn_date AS DATE)) * 100 + 
        MONTH(CAST(churn_date AS DATE))
) -- CTE to count how many users churned each month 
-- Combine both sets of data to calcuate net growth and total base 
SELECT 
    j.month_year,
    j.new_subscribers,
	-- COALESCE handles months where 0 users churned 
    COALESCE(c.churned_users, 0)                    AS churned_users,
	-- Net Growth = New joins minus those who churned 
    j.new_subscribers - COALESCE(c.churned_users, 0) AS net_growth,
	-- Running total of subscribers base over time 
    SUM(j.new_subscribers - COALESCE(c.churned_users, 0))
        OVER (ORDER BY j.month_year)                AS running_total_subscribers
FROM monthly_joins j
-- Using LEFT JOIN to ensure every month we had signups even if noone churned as in to not skip any month at all using inner join we would have skipped months 
LEFT JOIN monthly_churns c ON j.month_year = c.month_year
ORDER BY j.month_year;





/*Q2. REVENUE BY SUBSCRIPTION PLAN 
Business Question: What is the total and average monthly  revenue generated by each subscription plan?*/
SELECT 
     subscription_plan,
	 COUNT(user_id) AS total_subscribers , -- count of unique users in each subscription plan 
	 SUM (monthly_price_inr ) as total_revenue , -- total monthly revenue 
	 -- count of users in each plan / count of users accross all plans * 100 
	 ROUND(CAST(COUNT(user_id) AS FLOAT ) / SUM(COUNT(user_id)) OVER () *100,2) AS subscriber_share_pct, 
	 -- revenue generated from each plan / revenue generated from all the plans * 100 
	 ROUND(CAST(SUM(monthly_price_inr) AS FLOAT )/ SUM(SUM(monthly_price_inr)) OVER () * 100,2) AS revenue_share_pct

FROM subscribers
GROUP BY subscription_plan
ORDER BY total_revenue DESC; 

/* Q2 REVISED: ACTUAL REVENUE BY PLAN (SUCCESSFUL PAYMENTS ONLY) */
SELECT 
    plan_at_payment AS subscription_plan, -- Using the plan they had AT the time of payment
    COUNT(DISTINCT user_id) AS unique_paying_subscribers,
    SUM(amount_inr) AS total_actual_revenue, 
    
    -- Subscriber Share (Based on successful payments)
    ROUND(
        CAST(COUNT(DISTINCT user_id) AS FLOAT) / 
        SUM(COUNT(DISTINCT user_id)) OVER () * 100, 2
    ) AS subscriber_share_pct,
    
    -- Revenue Share (Actual money collected)
    ROUND(
        CAST(SUM(amount_inr) AS FLOAT) / 
        SUM(SUM(amount_inr)) OVER () * 100, 2
    ) AS revenue_share_pct

FROM payments
WHERE payment_status = 'Success' -- CRITICAL: Exclude failed payments
GROUP BY plan_at_payment
ORDER BY total_actual_revenue DESC;

/*Q3. CHURN RATE BY SUBSCRIPTION PLAN 
Business Question : Which subscrption plan contributes to most churn rate */

SELECT 
    subscription_plan,
    COUNT(user_id)                                   AS total_subscribers,
    SUM(CAST(churned AS INT))                        AS churned_users,
	-- churned users / total users * 100 
    ROUND(CAST(SUM(CAST(churned AS INT)) AS FLOAT) 
          / COUNT(user_id) * 100, 2)                 AS churn_rate_pct
FROM subscribers
GROUP BY subscription_plan
ORDER BY churn_rate_pct DESC;

/* TOP 5 STATES BY SUBSCRIBERS COUNT 
